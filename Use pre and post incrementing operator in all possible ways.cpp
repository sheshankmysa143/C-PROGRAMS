#include <stdio.h>

int main() {
    int a = 5, b = 5;

    printf("Original a = %d, b = %d\n", a, b);
    printf("Post-increment a++ = %d\n", a++);
    printf("After post-increment, a = %d\n", a);
    printf("Pre-increment ++b = %d\n", ++b);
    printf("After pre-increment, b = %d\n", b);

    return 0;
}

