#include <stdio.h>

int main() {
    int a = 10;
    float b = 3.14f;
    char c = 'A';
    double d = 123.456;

    printf("Integer: %d\n", a);
    printf("Float: %.2f\n", b);
    printf("Character: %c\n", c);
    printf("Double: %.3lf\n", d);

    return 0;
}

